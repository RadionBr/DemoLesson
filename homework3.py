# Користувач вводить рядок, Ваше завдання – перетворити рядок на hashtag.
#
# Декілька правил:
#
# ніяких символів з набору string.punctuation не повинно бути, у тому числі й пробілів;
# підсумкова довжина hashtag має бути не більше 140 символів.
# кожне слово починається з великої літери.
# якщо довжина фінішного хештегу більше 140 символів - обрізати підсумковий рядок до 140 символів.

import string

# Введення рядка від користувача
user_input = input("Введіть рядок: ")

# Видалення символів пунктуації та пробілів
cleaned_input = ''.join(char for char in user_input if char not in string.punctuation and not char.isspace())

# Переведення кожного слова у верхній регістр та додавання символу #
hashtag = '#' + cleaned_input.title()

# Обрізання до 140 символів, якщо потрібно
if len(hashtag) > 140:
    hashtag = hashtag[:140]

print("Hashtag:", hashtag)